from dataclasses import dataclass
from typing import Optional


@dataclass
class Location:
    """A location in our text adventure game world.

    Instance Attributes:
        - id_num: The unique integer identifier for this location.
        - brief_description: A short description shown when the player revisits this location.
        - long_description: A full description shown when the player visits this location
            for the first time or uses the "look" command.
        - available_commands: A mapping from command strings (e.g., "go north")
            to the id of the location reached by executing that command.
        - items: A list of names of items currently located at this location.
        - visited: Whether the player has visited this location before.

    Representation Invariants:
        - self.id_num >= 0
        - all(isinstance(cmd, str) for cmd in self.available_commands)
        - all(isinstance(loc_id, int) for loc_id in self.available_commands.values())
    """

    # This is just a suggested starter class for Location.
    # You may change/add parameters and the data available for each Location object as you see fit.
    #
    # The only thing you must NOT change is the name of this class: Location.
    # All locations in your game MUST be represented as an instance of this class.

    id_num: int
    brief_description: str
    long_description: str
    available_commands: dict[str, int]
    items: list[str]
    visited: bool = False


@dataclass
class Item:
    """An item in our text adventure game world.

    Instance Attributes:
        - name: The name of this item.
        - description: A description shown when the player examines or picks up the item.
        - start_position: The id of the location where this item initially appears.
        - target_position: The id of the location where this item should be deposited
            to earn points.
        - target_points: The number of points earned when this item is deposited
            at its target location.
        - fixed: Whether this item is fixed in place and cannot be picked up.
        - locked: Whether this item is locked and requires some condition to interact with.
        - messages: An optional message shown when the player interacts with the item
            while it is fixed or locked.
        - reveals_flag: An optional string flag revealed when this item is successfully used.


    Representation Invariants:
        - self.target_points >= 0
    """

    # NOTES:
    # This is just a suggested starter class for Item.
